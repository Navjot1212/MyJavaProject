
public class NewMethod {
	int num1 = 10;
	int num2 = 20;

	void sumOfTwoNumbers() {
		int sum = num1 + num2;
		System.out.println("sum of two numbers is " + sum);
	}

	void differenceOfTwoNumbers() {
		int subtraction = num2 - num1;
		System.out.println("sum of two numbers is " + subtraction);
	}

	void productOfTwoNumbers() {
		int product = num1 * num2;
		System.out.println("sum of two numbers is " + product);
	}
}
